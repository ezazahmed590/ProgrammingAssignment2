## Write a short comment describing this function
#  Function"makeCacheMatrix" will only take a matrix as an input (which we assume
#  to be invertible in this assignment. The function will give us an output of a 
#  list containging 4 functions- set(), get(), setinv(), getinv(). The 4 functions would serve the
# the following purposes consecutively- 1. setting the matrix to be inversed,
#                                       2. Getting the value of the matrix inputted,
#                                       3. setting the value of the inverse matrix,
#                                       4. Getting the value of the inverse matrix.
# These function would be called upon in the "CacheSolve" function to perform thier 
# intended tasks.The "x" vector will store the value of the matrix input and "i"
# vector (the vector intended to cache the value of the inverse) will be reset to 
# NULL value everytime we we input something in the makeCacheMatrix() function.

makeCacheMatrix <- function(x = matrix()) {
        i <- NULL
        set <- function(y) {
                x <<- y
                i <<- NULL
        }
        get <- function() x
        setinv <- function(inverse) i <<- inverse 
        getinv <- function() i
        list(set = set, get = get,
             setinv = setinv,
             getinv = getinv)

}


