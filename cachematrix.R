#        generated by the first function as an output. The function will then either
#        calculate the inverse of the inputted matrix, or fetch the cached value of
#        the inverse matrix if it has been calculated before.

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
        i <- x$getinv()
        if(!is.null(i)) {
                message("getting cached data")
                return(i)
        }
        data <- x$get()
        i <- solve(data, ...)
        x$setinv(i)
        i
}
